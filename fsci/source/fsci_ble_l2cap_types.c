void fsciBleL2capGetConfigStructFromBuffer(l2caConfigStruct_t* pConfigStruct, uint8_t** ppBuffer)
{
    /* Read l2caConfigStruct_t structure fields from buffer */
    fsciBleGetUint32ValueFromBuffer(pConfigStruct->leFeatures, *ppBuffer);
    fsciBleGetUint32ValueFromBuffer(pConfigStruct->hciLeBufferSize, *ppBuffer);
    fsciBleGetUint16ValueFromBuffer(pConfigStruct->maxPduPayloadSize, *ppBuffer);
}


void fsciBleL2capGetBufferFromConfigStruct(const l2caConfigStruct_t* pConfigStruct, uint8_t** ppBuffer)
{
    /* Write l2caConfigStruct_t structure fields in buffer */
    fsciBleGetBufferFromUint32Value(pConfigStruct->leFeatures, *ppBuffer);
    fsciBleGetBufferFromUint32Value(pConfigStruct->hciLeBufferSize, *ppBuffer);
    fsciBleGetBufferFromUint16Value(pConfigStruct->maxPduPayloadSize, *ppBuffer);
}

